<script>
    var ImgIndx=0;
    $(function(){
       $(".stateOneBox .fen").each(function(e){
           $(".fen").eq(e).click(function(){
               $(".fen").removeClass("fenClick");
               $(".fen").eq(e).addClass("fenClick");
           })
       });

        $(".content .upImg").each(function(e){
            $('.upImg').eq(e).click(function(){
                ImgIndx=e;
                $("#fileImage").click()
            });
        });
    });
    //限制字数
    function ziFun(e){
        var obj=$(e);
        var maxL=obj.val().length;
        var maxV=obj.val();
        var maxHintL=200;
        maxL=maxHintL-maxL>=0?maxHintL-maxL:0;
        maxV=maxV.substr(0, 200);
        obj.val(maxV);
        obj.next().text(maxL)
    }


    //上传图片
    function  imgFun(){
        if($(".upAllImg .upImgBox").length>=5){
            layer.msg('最多上传五张', {
                time: 1000
            });
            return;
        }

        var file = $('#fileImage')[0].files[0];
        var reader = new FileReader();
        if (file.size > 5000000) { // 限制图片大小，不能超过5M
            layer.msg('最多上传5M的图片', {
                time: 500
            });
            return;
        }
        reader.onload = (function (theFile) {
            return function (e) {

                var str='<div class="upImgBox z pr" style="background-image:url('+e.target.result+')">'
                        +'<div class="deleteBtn pa" onclick="deleteFun(this)">'
                        +'</div>'
                        +'</div>';
                $(".upAllImg").eq(ImgIndx).append(str);
            }
        })(file);
        reader.readAsDataURL(file);
    };

    //删除图片
    function deleteFun(e){
        layer.confirm('确定删除？', {
            btn: ['是的','取消'] //按钮
        }, function(){
            layer.msg('已删除', {
                time: 500
            });
            $(e).parent().remove();
        }, function(){
            layer.msg('取消删除', {
                time: 500
            });
        });
    };
</script>
