            $(".delete").eq(e).click(function(){
                $(".masking").show();
                $(".neirong2").show();
                $(".neirong").hide();
                //取消删除
                $(".delete2Yes").click(function(){
                    $(".masking").hide();
                    $(".neirong2").hide();
                    $(".neirong").show();

                });
                //确定删除
                $(".delete2No").click(function(){
                    $(".masking").hide();
                    $(".neirong2").hide();
                    $(".neirong").show();
                    $(".template").eq(e).remove();
                });
            });
        });
        $(".masking").css({
           height:$(window).height()
        });
        //文字行高
        $(".listAll .template").each(function(e){
            var num=$(".template").eq(e).find(".goodsList").length;
            $(".goodsOther").eq(e).css({
                height:num*120+"px"
            });
            $(".templateMoney2D").eq(e).css({
                lineHeight:num*120+"px"
            });
            $(".templateStateD").eq(e).css({
                lineHeight:num*120+"px"
            });
        });

        //取票地址查询
        $(".listAll .qu").each(function(e){
            $(".qu").eq(e).click(function(){
                $(".qupiao,.wuliu").hide();
                $(".qupiao").eq(e).show();
            });
        });

        //物流信息查询
        $(".listAll .cha").each(function(e){
            $(".cha").eq(e).click(function(){
                $(".qupiao,.wuliu").hide();
                $(".wuliu").eq(e).show();
            });
        });
        $(".qupiaoClose,.wuliuClose").click(function(){
            $(".qupiao,.wuliu").hide();
        });

        //取消订单
        $(".template .cancel").each(function(e){
            $(".cancel").eq(e).click(function(){
                $(".masking").show();
                $(".neirong").show();
                $(".neirong2").hide();

                //抛弃
                $(".deleteYes").click(function(){
                    $(".masking").hide();
                    $(".neirong2").hide();
                    $(".neirong").show();
                    $(".templateStateD").eq(e).text("取消订单");
                    $(".templateHandle2D").eq(e).text("");
                    $(".templateLast").eq(e).hide();
                });
                //留下
                $(".deleteNo").click(function(){
                    $(".masking").hide();
                    $(".neirong").hide();
                    $(".neirong2").show()
                });


            });
        })
    });


