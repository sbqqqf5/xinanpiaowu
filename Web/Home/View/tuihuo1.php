<script>
    $(function(){
        $('.upImg').click(function(){
            $("#fileImage").click()
        });
        //点击提交运单后
        $(".tijiaoBtn").click(function(){
            if($(this).attr("data-state")==1){
                $(".tijiaoBtn").text("修改订单");
                $(".stateThreeBox input").attr("readonly","readonly");
                $(".stateThreeBox input").css({
                    border:"1px solid #fafafa",
                    backgroundColor:"#fafafa"
                });
                $(".threeIcon").show();
                $(this).attr("data-state",2);
            }else {
                $(".tijiaoBtn").text("提交订单");
                $(".stateThreeBox input").removeAttr("readonly");
                $(".stateThreeBox input").css({
                    border:"1px solid #d8d8d8",
                    backgroundColor:"#fff"
                });
                $(".threeIcon").hide();
                $(this).attr("data-state",1);
            }
        })
    });
    //上传图片
    function  imgFun(){
        if($(".upAllImg .upImgBox").length>=5){
            layer.msg('最多上传五张', {
                time: 1000
            });
            return;
        }

        var file = $('#fileImage')[0].files[0];
        var reader = new FileReader();
        if (file.size > 5000000) { // 限制图片大小，不能超过5M
            layer.msg('最多上传5M的图片', {
                time: 500
            });
            return;
        }
        reader.onload = (function (theFile) {
            return function (e) {

                var str='<div class="upImgBox z pr" style="background-image:url('+e.target.result+')">'
                    +'<div class="deleteBtn pa" onclick="deleteFun(this)">'
                    +'</div>'
                +'</div>';
                $(".upAllImg").append(str);
            }
        })(file);
        reader.readAsDataURL(file);
    };

    //删除图片
    function deleteFun(e){
        layer.confirm('确定删除？', {
            btn: ['是的','取消'] //按钮
        }, function(){
            layer.msg('已删除', {
                time: 500
            });
            $(e).parent().remove();
        }, function(){
            layer.msg('取消删除', {
                time: 500
            });
        });
    };

    //限制字数
    function ziFun(){
        var maxL=$(".shuoming").val().length;
        var maxV=$(".shuoming").val();
        var maxHintL=200;
        maxL=maxHintL-maxL>=0?maxHintL-maxL:0;
        maxV=maxV.substr(0, 200);
        $(".shuoming").val(maxV);
        $(".maxZi").text(maxL)
    }
